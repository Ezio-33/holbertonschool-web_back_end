#!/usr/bin/env python3
"""
Module pour convertir un flottant en chaîne de caractères avec annotations de type.
"""

def to_str(n: float) -> str:
    """
    Retourne la représentation en chaîne de caractères d'un nombre flottant.

    Arguments:
    n -- nombre flottant

    Return:
    La représentation en chaîne de caractères du nombre.
    """
    return str(n)
