#!/usr/bin/env python3
"""
Module pour concaténer deux chaînes de caractères avec annotations de type.
"""

def concat(str1: str, str2: str) -> str:
    """
    Retourne la concaténation de deux chaînes de caractères.

    Arguments:
    str1 -- première chaîne de caractères
    str2 -- seconde chaîne de caractères

    Return:
    La concaténation des deux chaînes en tant que chaîne de caractères.
    """
    return str1 + str2
